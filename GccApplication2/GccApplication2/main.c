#define F_CPU 8000000UL

#include <avr/io.h>
#include <avr/interrupt.h>

// Обработчик прерывания компаратора
ISR(ANA_COMP_vect)
{
    PORTD ^= (1 << PD0);   
}

int main(void)
{
    // --- Настройка LED ---
    DDRD |= (1 << PD0);     // PD0 как выход
    PORTD &= ~(1 << PD0);   // LED изначально выкл

    // --- Настройка входа AIN1 (PB3) ---
    DDRB &= ~(1 << PB3);    // PB3 как вход
    // Можно включить подтяжку (если вход "в воздухе")
    // PORTB |= (1 << PB3);

    // --- Настройка компаратора ---
    ACSR &= ~(1 << ACD);    // включить компаратор
    ACSR |= (1 << ACBG);    // AIN0 = внутренняя опора 1.22V

    // Очистка флага прерывания (на всякий случай)
    ACSR |= (1 << ACI);

    // Прерывание по ЛЮБОМУ изменению выхода компаратора
    ACSR |= (1 << ACIS1);
    ACSR &= ~(1 << ACIS0);

    ACSR |= (1 << ACIE);    // разрешить прерывание компаратора

    sei();                  // глобальные прерывания

    // --- Основной цикл ---
    while (1)
    {
        // ничего не делаем — всё через прерывания
    }
}